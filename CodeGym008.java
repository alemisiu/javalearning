public class CodeGym008 {




        public static void main(String[] args) {
            //tutaj wpisz swój kod
           for (int i = 1; i <= 10; i++) {   // Pętla zewnętrzna
                for (int j = 1; j <= 10; j++) { // Pętla wewnętrzna (spacja poprawiona)
                    System.out.print(i * j + " "); // Wyświetla wynik mnożenia
                }
                
                System.out.println();
                }
        }
    }
    